def add(a,b):
    # add two numbers and return the result
    return a + b
def substract(a,b):
    # substract b from a and return the result
    return a - b

# test our functions
print(f"5 +3 = {add(5, 3)}")
print(f"10 - 4 = {substract(10,4)}")

def multiply(a, b):
    # multiply two numbers and return the result
    return a * b
# test new function
print(f"4 * 6 = {multiply(4, 6)}")